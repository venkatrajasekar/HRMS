package Encapulsation;
public class Student {
    //private data member
    private String name;
    private Integer age;

    //getter method for name
    public String getName() {
        return name;
    }
    public Integer getAge(){
        return age;
    }


    //setter method for name
    public void setName(String name) {
        this.name = name;
    }
    public void setAge(Integer age){
        this.age=age;
    }
}

